
/**
 * op_add - sum two digits.
 * @a: 1st number.
 * @b: 2nd number.
 *
 * Return: sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference two digits.
 * @a: 1st number.
 * @b: 2nd number.
 *
 * Return: sub of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product of two digits.
 * @a: 1st number.
 * @b: 2nd number.
 *
 * Return: product of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_mod - remainder of the division of two digits.
 * @a: 1st number.
 * @b: 2nd number.
 *
 * Return: mod of a and b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}

/**
 * op_div - result of the division of two digits.
 * @a: 1st number.
 * @b: 2nd number.
 *
 * Return: division of a and b.
 */
int op_div(int a, int b)
{
	return (a / b);
}
